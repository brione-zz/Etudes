  def alert(patient_teeth) do
    alert_teeth(patient_teeth, 1, [])
  end

  def alert_teeth([], _, alerts) do
    Enum.reverse alerts
  end

  def alert_teeth([h|t], n, alerts) do
    if has_bad_pockets?(h) do
      alert_teeth(t, n + 1, [ n | alerts])
    else
      alert_teeth(t, n + 1, alerts)
    end
  end

  def has_bad_pockets?(pockets) do
    Stats.maximum(pockets) >= 4
  end

end
